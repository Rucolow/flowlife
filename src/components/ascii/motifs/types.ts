/**
 * モチーフ関数の型定義。
 *
 * 各モチーフは AsciiCanvas によって 1 セルずつ呼び出され、
 * 返り値(0-1)はディザリングと文字マッピングに使われる。
 *
 * - 0       : 描画しない（背景が見える）
 * - 0.01-0.3: 形のエッジ（疎・低アルファ）
 * - 0.3-0.7 : 形の中間部
 * - 0.7-1.0 : 形の中心（密・高アルファ）
 */
export type MotifFunction = (
  /** 正規化X座標 (0-1, 左端=0 / 右端=1) */
  nx: number,
  /** 正規化Y座標 (0-1, 上端=0 / 下端=1) */
  ny: number,
  /** 経過時間（秒） */
  time: number,
  /** マウスX (0-1) — hero モードのみ意味を持つ。card モードでは 0.5 固定。 */
  mx: number,
  /** マウスY (0-1) — hero モードのみ意味を持つ。card モードでは 0.5 固定。 */
  my: number,
) => number;

/** 統一文字セット。index 0 の空白は描画されない。 */
export const CHARS = " .,:;+*?%S#@";

/** Bayer 4x4 ディザマトリクス（展開済み）。 */
export const BAYER4: readonly number[] = [
  0, 8, 2, 10, 12, 4, 14, 6, 3, 11, 1, 9, 15, 7, 13, 5,
];
